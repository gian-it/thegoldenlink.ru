 * @method static \Illuminate\Database\Eloquent\Builder|User wherePhone($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereRememberToken($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereSex($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereSpiritualName($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class User extends Authenticatable implements HasMedia, FilamentUser, HasAvatar
{
    use HasApiTokens;
    use Notifiable;
    use HasFactory;
    use HasRoles;
    use InteractsWithMedia;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'spiritual_name',
        'email',
        'sex',
        'phone',
        'is_subscribed',
        'password',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'sex'               => Sex::class,
        'email_verified_at' => 'immutable_datetime',
        'is_subscribed'     => 'boolean',
    ];

    /** Help Methods */

    public function isAdmin(): bool
    {
        return $this->hasRole('admin');
    }

    public function canAccessFilament(): bool
    {
        if ($this->hasPermissionTo('cabinet.auth')) {
            return true;
        }

        if ($this->isAdmin()) {
            return true;
        }

        return false;
    }

    public function getFilamentAvatarUrl(): ?string
    {
        return $this->getFirstMedia('avatar')?->getUrl();
    }

    /**  Accessors & Mutators */

    public function password(): Attribute
    {
        return Attribute::make(
            set: fn ($value) => !$value ? $this->password : $value,
        );
    }
}
